// Constructors
StringView::StringView ()
  : internal_string_view {}
  , wchar_t_DataBuffer ()
{
}

StringView::StringView (const char* c_str)
  : internal_string_view { c_str }
  , wchar_t_DataBuffer (std::strlen (c_str) + 1)
{
}

StringView::StringView (const std::string_view& str_v)
  : internal_string_view { str_v }
  , wchar_t_DataBuffer (str_v.length () + 1)
{
}

